package com.example.woolwichshivajishakha;

public class PointValue
{
    int xValue,yValue;

    public PointValue(int xValue, int yValue) {
        this.xValue = xValue;
        this.yValue = yValue;
    }

    public int getxValue(){
        return xValue;
    }

    public int getyValue() {
        return yValue;
    }
}
